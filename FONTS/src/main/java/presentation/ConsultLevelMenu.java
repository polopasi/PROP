    /**
     * Initializes the view. Loads all the levels of the database to show them.
     */
    public void init() {
        model.clear();
        levelsId.clear();
        jList1.removeAll();
        jList1.setModel(model);
        List<List<Level>> levels = this.viewController.getLevelsBySize();
        int numLevels = 0;
        for (List<Level> levelGroup : levels) {
            String levelSize = "[Size " + levelGroup.get(0).getSize() + "]";
            for (Level level : levelGroup) {
                numLevels += 1;
                String levelName = levelSize.concat(" - " + numLevels + " (" + level.getCreator() + ")");
                model.addElement(levelName);
                levelsId.add(level.getId());
            }
        }
    }

