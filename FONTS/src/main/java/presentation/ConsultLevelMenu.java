    /**
     * Opens a new view that contains the information for the selected level.
     */
    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        int index = jList1.locationToIndex(evt.getPoint());
        if (index >= 0 && index < levelsId.size()) {
            int selectedItem = levelsId.get(index);

            this.viewController.getLevelsBySize().stream()
                .flatMap(List::stream)
                .filter(level -> level.getId() == selectedItem)
                .findFirst()
                .ifPresent(level -> viewController.LevelInfoView(level.getId()));
        }
    }//GEN-LAST:event_jList1MouseClicked


